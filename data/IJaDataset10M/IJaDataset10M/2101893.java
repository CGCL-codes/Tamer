package javaapplication;

public class Cucina {

    private int CucinaID;

    private String Nome;

    protected Cucina() {
    }

    public int getCucinaID() {
        return CucinaID;
    }

    public void setCucinaID(int CucinaID) {
        this.CucinaID = CucinaID;
    }

    public String getNome() {
        return Nome;
    }

    public void setNome(String Nome) {
        this.Nome = Nome;
    }
}
