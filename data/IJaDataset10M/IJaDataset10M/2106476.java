    private void BotonModificarActionPerformed(java.awt.event.ActionEvent evt) {
        if (validado()) {
            PersonaEmpresaInterfazService proveedorService = new PersonaEmpresaImplementsService();
            ft.setNombres(razon.getText());
            ft.setDniRuc(ruc.getText());
            ft.setEmail(email.getText());
            ft.setTelefono(telefono.getText());
            ft.setDireccion(direccion.getText());
            ft.setDescripcion(descripcion.getText());
            ft.setCreatedAt(new Date());
            ft.setUpdatedAt(new Date());
            ft.setContacto(contacto.getText());
            ft.setTelefonoContacto(telefono_contacto.getText());
            proveedorService.setEntityManagerFactory(PanelPrincipal.emf);
            proveedorService.modificar(ft);
            JOptionPane.showMessageDialog(this, "Proveedor Modificado Correctamente", "Modificar Proveedor", JOptionPane.INFORMATION_MESSAGE);
            guardado = true;
            this.setVisible(false);
        } else JOptionPane.showMessageDialog(this, error, "Error", JOptionPane.WARNING_MESSAGE);
    }

    private void BotonEliminarActionPerformed(java.awt.event.ActionEvent evt) {
        PersonaEmpresaInterfazService workservice = new PersonaEmpresaImplementsService();
        workservice.setEntityManagerFactory(PanelPrincipal.emf);
        int confirmado = JOptionPane.showConfirmDialog(null, "¿Esta Seguro que desea eliminar este registro?");
        if (JOptionPane.OK_OPTION == confirmado) {
            workservice.eliminar(ft);
            JOptionPane.showMessageDialog(this, "El registro ha sido eliminado.", "Eliminar Registro", JOptionPane.INFORMATION_MESSAGE);
            modificado = true;
            this.setVisible(false);
        } else {
            System.out.println("vale... no borro nada...");
        }
    }

    private void BotonCancelarActionPerformed(java.awt.event.ActionEvent evt) {
        this.setVisible(false);
        cancelado = true;
    }

    private void BotonBuscarActionPerformed(java.awt.event.ActionEvent evt) {
        if (buscar.getText().length() == 11) {
            PersonaEmpresaInterfazService workservice = new PersonaEmpresaImplementsService();
            workservice.setEntityManagerFactory(PanelPrincipal.emf);
            ft = (FidProveedor) workservice.buscar("fidProveedor", "dniRuc", buscar.getText());
            if (ft != null) {
                ActualizarCampos();
                ActivarOrBloquearOpciones(true);
            } else {
                JOptionPane.showMessageDialog(this, "No se encontrarn resultados", "Error", JOptionPane.WARNING_MESSAGE);
                ActivarOrBloquearOpciones(false);
                Restablecer();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Ingrese RUC de 11 difitos", "Error", JOptionPane.WARNING_MESSAGE);
            ActivarOrBloquearOpciones(false);
            Restablecer();
        }
    }

    private void buscarKeyTyped(java.awt.event.KeyEvent evt) {
        if (buscar.getText().length() < 11) {
            SoloDigito(evt);
        } else {
            evt.consume();
        }
    }

    private void ActualizarCampos() {
        razon.setText(ft.getNombres());
        ruc.setText(ft.getDniRuc());
        email.setText(ft.getEmail());
        telefono.setText(ft.getTelefono());
        direccion.setText(ft.getDireccion());
        descripcion.setText(ft.getDescripcion());
        contacto.setText(ft.getContacto());
        telefono_contacto.setText(ft.getTelefonoContacto());
    }

    private void ActivarOrBloquearOpciones(boolean val) {
        BotonModificar.setEnabled(val);
        BotonEliminar.setEnabled(val);
        razon.setEditable(val);
        ruc.setEditable(val);
        email.setEditable(val);
        telefono.setEditable(val);
        direccion.setEditable(val);
        descripcion.setEditable(val);
        contacto.setEditable(val);
        telefono_contacto.setEditable(val);
    }

    private void Restablecer() {
        razon.setText("");
        ruc.setText("");
        telefono.setText("");
        email.setText("");
        direccion.setText("");
        descripcion.setText("");
        contacto.setText("");
        telefono_contacto.setText("");
    }

    private void SoloTexto(java.awt.event.KeyEvent evt) {
        char c = evt.getKeyChar();
        if (Character.isDigit(c)) evt.consume();
    }

    private void SoloDigito(java.awt.event.KeyEvent evt) {
        char c = evt.getKeyChar();
        if (!Character.isDigit(c)) evt.consume();
    }

    public boolean validado() {
        error = "";
        if (razon.getText().length() == 0) error += "- Ingrese Razon Social\n";
        if (ruc.getText().length() != 11) error += "- Ingrese RUC (11 digitos)\n";
        if (contacto.getText().length() == 0) error += "- Ingrese Nombre de Contacto\n";
        if (error.length() == 0) return true;
        return false;
    }

