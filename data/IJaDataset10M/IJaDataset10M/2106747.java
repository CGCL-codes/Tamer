package jsesh.importer.rtf;

public class RTFImportException extends Exception {

    public RTFImportException() {
        super();
    }

    public RTFImportException(String message, Throwable cause) {
        super(message, cause);
    }

    public RTFImportException(String message) {
        super(message);
    }

    public RTFImportException(Throwable cause) {
        super(cause);
    }
}
