    private void jButton45MouseClicked(java.awt.event.MouseEvent evt) {
        try {
            list1 = (DefaultListModel) jList1.getModel();
            boolean flag = extra.CheckStudentCourse(ID, list1);
            if (flag) {
                Object[] list = list1.toArray();
                int[] crsIDs = new int[list.length];
                for (int i = 0; i < list.length; i++) {
                    query = prc.GetCourseID(list[i].toString());
                    r = c.connection(query);
                    while (r.next()) {
                        crsIDs[i] = Integer.parseInt(r.getString(1));
                    }
                }
                for (int i = 0; i < list.length; i++) {
                    query = prc.Assign_Course_to_Student(String.valueOf(ID), String.valueOf(crsIDs[i]));
                    check = c.UpdateConnection(query);
                }
                if (check == 1) JOptionPane.showMessageDialog(null, "Added Succefully", "Success Operation", JOptionPane.INFORMATION_MESSAGE); else JOptionPane.showMessageDialog(null, "Can't be Added", "Unsuccess Operation", JOptionPane.INFORMATION_MESSAGE);
            } else JOptionPane.showMessageDialog(this, "please check the precourses of the each course in the university Regulation", "Note", JOptionPane.OK_OPTION);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "ERROR" + ex.toString());
        }
    }

