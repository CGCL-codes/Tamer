    public CourseRegister(int stdID, Student f) {
        this.setLocation(200, 100);
        this.setResizable(false);
        this.setTitle("Courses Registeration Main Frame");
        ID = stdID;
        frame = f;
        initComponents();
        try {
            query = prc.get_Current_Year();
            r = c.connection(query);
            while (r.next()) {
                jLabel2.setText(r.getString(1));
            }
            query = prc.GetNumberofCourseforStudent(stdID);
            r = c.connection(query);
            while (r.next()) {
                count = Integer.parseInt(r.getString(1));
            }
            int[] crsIDs = new int[count];
            query = prc.GetTakensCourseID(stdID);
            r = c.connection(query);
            for (int i = 0; r.next(); i++) {
                crsIDs[i] = Integer.parseInt(r.getString(1));
            }
            for (int i = 0; i < count; i++) {
                query = prc.GetCourseName(crsIDs[i]);
                r = c.connection(query);
                while (r.next()) {
                    list2.addElement(r.getString(1));
                }
            }
            jList2.setModel(list2);
            query = prc.GetAllCourseName();
            r = c.connection(query);
            while (r.next()) {
                jComboBox1.addItem(r.getString(1));
            }
            Object[] temp = list2.toArray();
            for (int i = 0; i < temp.length; i++) {
                jComboBox1.removeItem(temp[i]);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "ERROR" + ex.toString());
        }
        jButton45.setVisible(false);
    }

