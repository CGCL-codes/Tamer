    private void jTextCodigoKeyPressed(java.awt.event.KeyEvent evt) {
        if (!jTextCodigo.getText().matches("([0-9--])*")) {
            JOptionPane.showMessageDialog(null, "Dato Invalido Solo Numeros", "Error", JOptionPane.ERROR_MESSAGE);
            jTextCodigo.setText("");
        }
    }

    private void jTextCodigoKeyReleased(java.awt.event.KeyEvent evt) {
        if (!jTextCodigo.getText().matches("([0-9--])*")) {
            JOptionPane.showMessageDialog(null, "Dato Invalido Solo Numeros", "Error", JOptionPane.ERROR_MESSAGE);
            jTextCodigo.setText("");
        }
    }

    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {
        if (jComboBox1.getSelectedItem().equals("Codigo")) {
            jTextCodigo.setEditable(true);
            jXDatePicker1.setEditable(false);
        } else {
            jXDatePicker1.setEditable(true);
            jTextCodigo.setEditable(false);
        }
    }

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {
        GestorVista_ModificarPedidos.mostrarDetalleSolicitud(jTable1, jTable2, dtm, dtm2);
    }

    private void jTable2KeyReleased(java.awt.event.KeyEvent evt) {
        if (evt.getKeyCode() == KeyEvent.VK_DELETE) {
            GestorVista_ModificarPedidos.QuitarProductos(jTable2, dtm2);
        }
    }

