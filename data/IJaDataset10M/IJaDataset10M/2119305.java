    private void OKButtonActionPerformed(java.awt.event.ActionEvent evt) {
        try {
            int saveInterval = Integer.parseInt(IntTextField.getText());
            if (saveInterval <= 0) throw new NumberFormatException("positive integer required");
            app.project.setAutoSaveInterval(saveInterval * 60);
        } catch (NumberFormatException e) {
            app.showErrorDialog("Enter a positive integer for the auto save interval.");
            return;
        }
        app.project.setDeleteAutoSaveFile(DelCheckBox.isSelected());
        setVisible(false);
    }

