package com.luxoft.fitpro.plugin.htmleditor.model;

public class TemplateBuilderException extends Exception {

    /**
     * 
     */
    private static final long serialVersionUID = -8582645161390440595L;

    public TemplateBuilderException() {
        super();
    }

    public TemplateBuilderException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public TemplateBuilderException(String arg0) {
        super(arg0);
    }

    public TemplateBuilderException(Throwable arg0) {
        super(arg0);
    }
}
