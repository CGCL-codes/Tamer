    private void jbProcediMousePressed(java.awt.event.MouseEvent evt) {
        String ERR = "";
        if (!jcFunzioni.checkEmail(jtfA.getText())) {
            ERR += "E-mail destinatario errata !\n";
            jtfA.setBackground(Color.PINK);
        } else jtfA.setBackground(Color.WHITE);
        if (jtfOggetto.getText().length() == 0) {
            ERR += "Oggetto vuoto !\n";
            jtfOggetto.setBackground(Color.PINK);
        } else jtfOggetto.setBackground(Color.WHITE);
        if (ERR.length() != 0) {
            JOptionPane.showMessageDialog(this, "Sono stati trovati i seguenti errori:\n" + ERR, "Errore", JOptionPane.ERROR_MESSAGE);
        } else {
            errori = false;
            this.dispose();
        }
    }

