package org.redwood.business.report.reportgeneration.specificationsgroups.exceptions;

/**
 *
 * @author  Gerrit Franke
 * @version 1.0
 */
public class SeriesNotFoundException extends java.lang.Exception {

    /**
     * Creates new <code>SeriesNotFoundException</code> without detail message.
     */
    public SeriesNotFoundException() {
    }

    /**
     * Constructs an <code>SeriesNotFoundException</code> with the specified detail message.
     * @param msg the detail message.
     */
    public SeriesNotFoundException(String msg) {
        super(msg);
    }
}
