package com.proyecto.tropero.core.excepciones;

public class CancelarLimpiar extends Exception {

    public CancelarLimpiar() {
        super();
    }

    public CancelarLimpiar(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public CancelarLimpiar(String arg0) {
        super(arg0);
    }

    public CancelarLimpiar(Throwable arg0) {
        super(arg0);
    }
}
