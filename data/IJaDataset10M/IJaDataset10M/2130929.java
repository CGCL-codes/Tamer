    public void listarArticulos() throws SQLException, Exception {
        dtm.setNumRows(0);
        ControlArticulos.listarArticulo();
        Iterator it = ControlArticulos.getArticulo().iterator();
        while (it.hasNext()) {
            Articulo a = (Articulo) it.next();
            String[] fila = { a.getCodigo() + "", a.getNombre() };
            dtm.addRow(fila);
        }
    }

