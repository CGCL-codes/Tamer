    private void jTListarArticulosMouseClicked(java.awt.event.MouseEvent evt) {
        int fila = jTListarArticulos.rowAtPoint(evt.getPoint());
        int x = jTListarArticulos.getSelectedRow();
        if (fila < 0) {
            JOptionPane.showMessageDialog(null, "Seleccione primero el Producto");
        } else {
            try {
                String nom = (dtm.getValueAt(x, 0).toString());
                Productos p = ControlProductos.BuscarNombre(nom);
            } catch (ArrayIndexOutOfBoundsException e) {
                System.out.println(e);
            }
        }
    }

