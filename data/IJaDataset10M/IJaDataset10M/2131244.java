package com.germinus.xpression.groupware;

public class AuthorizatorException extends Exception {

    public AuthorizatorException() {
        super();
    }

    public AuthorizatorException(String message, Throwable cause) {
        super(message, cause);
    }

    public AuthorizatorException(String message) {
        super(message);
    }

    public AuthorizatorException(Throwable cause) {
        super(cause);
    }

    private static final long serialVersionUID = -929942538013321687L;
}
