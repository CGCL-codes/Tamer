package de.evandor.easyc.bundlesGenerator;

public class AttributeDescriptor {

    private String name;

    private String type;

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return this.name;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getType() {
        return this.type;
    }
}
