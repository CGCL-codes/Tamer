package biz.ivanov.ant4pde;

/**
 * @author arcivanov
 */
public class Ant4PdeException extends Exception {

    private static final long serialVersionUID = 1L;

    public Ant4PdeException() {
        super();
    }

    public Ant4PdeException(String message) {
        super(message);
    }

    public Ant4PdeException(String message, Throwable cause) {
        super(message, cause);
    }

    public Ant4PdeException(Throwable cause) {
        super(cause);
    }
}
