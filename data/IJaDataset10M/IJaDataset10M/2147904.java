package org.jazzteam.modelPC;

public class Os {

    private String name;

    private int decimalSystem;

    private int systemFiles;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getDecimalSystem() {
        return decimalSystem;
    }

    public void setDecimalSystem(int decimalSystem) {
        this.decimalSystem = decimalSystem;
    }

    public int getSystemFiles() {
        return systemFiles;
    }

    public void setSystemFiles(int systemFiles) {
        this.systemFiles = systemFiles;
    }
}
