package Clases;

public class Candado extends Grupo {

    private int Codigo;

    public Candado() {
        Codigo = 0;
    }

    /**
     * @return the Codigo
     */
    public int getCodigo() {
        return Codigo;
    }

    /**
     * @param Codigo the Codigo to set
     */
    public void setCodigo(int Codigo) {
        this.Codigo = Codigo;
    }
}
