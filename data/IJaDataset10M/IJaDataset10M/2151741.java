    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow == -1) {
            jLabel1.setText("Geen product geselecteerd.");
        } else {
            int selectedProduct = Integer.parseInt(jTable1.getValueAt(selectedRow, 0).toString());
            WinkelApplication.getInstance().showPanel(new ProductMod(selectedProduct));
        }
    }

