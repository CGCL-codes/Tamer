    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {
        int flag = 1;
        nome = jTextField1.getText();
        taxaBase = Double.parseDouble(jTextField2.getText());
        precoKM = Double.parseDouble(jTextField3.getText());
        if (nome == null || nome.equals("")) {
            flag = 0;
            JOptionPane.showMessageDialog(null, "Erro: nome fornecido inválido");
        }
        if (taxaBase < 0) {
            flag = 0;
            JOptionPane.showMessageDialog(null, "Erro: Taxa base fornecida inválida");
        }
        if (precoKM < 0) {
            flag = 0;
            JOptionPane.showMessageDialog(null, "Erro: Preço por quilômetro inválido");
        }
        if (flag == 1) {
            ID = persistencia.PersistenciaTipoLocacao.geraIDTipoLocacao();
            dominio.TipoLocacao tl = new dominio.TipoLocacao(nome, taxaBase, ID);
            tl.setPrecoKM(precoKM);
            persistencia.PersistenciaTipoLocacao.gravarobjeto(tl);
            SimpleDateFormat formatter = new SimpleDateFormat("HH:mm:ss");
            log.info("Tipo de locação " + nome + " cadastrada as " + formatter.format(new Date()));
            dispose();
        }
    }

