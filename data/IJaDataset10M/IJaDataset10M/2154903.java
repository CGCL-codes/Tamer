    public void setContactDetailTypeValues(Collection p_contactDetailTypeValues) {
        m_modelContactDetailType.addValues(p_contactDetailTypeValues);
    }

    public Collection getContactDetailTypeValues() {
        return m_modelContactDetailType.getElements();
    }

    public void setEditable(boolean p_editable, NotificationMechanismValue p_defaultNotificationMechanism) {
        m_editable = p_editable;
        m_modelContactDetailType.setEditable(p_editable, p_defaultNotificationMechanism);
    }

    private void checkPopup(java.awt.event.MouseEvent e) {
        if (m_editable && e.isPopupTrigger()) {
            m_selectedRow = jTableTypes.getSelectedRow();
            jMenuItemDelete.setEnabled(true);
            jMenuItemInsertAbove.setEnabled(true);
            jMenuItemMoveUp.setEnabled(true);
            jMenuItemMoveDown.setEnabled(true);
            if (m_selectedRow < 0) {
                jMenuItemDelete.setEnabled(false);
                jMenuItemInsertAbove.setEnabled(false);
                jMenuItemMoveUp.setEnabled(false);
                jMenuItemMoveDown.setEnabled(false);
            }
            if (m_selectedRow == 0) jMenuItemMoveUp.setEnabled(false);
            if (m_selectedRow == m_modelContactDetailType.getRowCount() - 1) jMenuItemMoveDown.setEnabled(false);
            jPopupMenu1.show(e.getComponent(), e.getX(), e.getY());
        }
    }

