package quest.reshanta;

import gameserver.model.gameobjects.Npc;
import gameserver.model.gameobjects.player.Player;
import gameserver.network.aion.serverpackets.SM_DIALOG_WINDOW;
import gameserver.questEngine.handlers.QuestHandler;
import gameserver.questEngine.model.QuestCookie;
import gameserver.questEngine.model.QuestState;
import gameserver.questEngine.model.QuestStatus;
import gameserver.utils.PacketSendUtility;

public class _2073CapturedComrades extends QuestHandler {

    private static final int questId = 2073;

    public _2073CapturedComrades() {
        super(questId);
    }

    @Override
    public void register() {
        qe.addQuestLvlUp(questId);
        qe.setNpcQuestData(278002).addOnTalkEvent(questId);
        qe.setNpcQuestData(278019).addOnTalkEvent(questId);
        qe.setNpcQuestData(278088).addOnTalkEvent(questId);
        qe.setNpcQuestData(215045).addOnTalkEvent(questId);
    }

    @Override
    public boolean onDialogEvent(QuestCookie env) {
        final Player player = env.getPlayer();
        final QuestState qs = player.getQuestStateList().getQuestState(questId);
        if (qs == null) return false;
        int var = qs.getQuestVarById(0);
        int targetId = 0;
        if (env.getVisibleObject() instanceof Npc) targetId = ((Npc) env.getVisibleObject()).getNpcId();
        if (qs.getStatus() == QuestStatus.START) {
            switch(targetId) {
                case 278002:
                    {
                        switch(env.getDialogId()) {
                            case 26:
                                if (var == 0) return sendQuestDialog(env, 1011);
                            case 10000:
                                if (var == 0) {
                                    qs.setQuestVarById(0, var + 1);
                                    updateQuestStatus(env);
                                    PacketSendUtility.sendPacket(player, new SM_DIALOG_WINDOW(env.getVisibleObject().getObjectId(), 10));
                                    return true;
                                }
                        }
                    }
                    break;
                case 278088:
                    {
                        switch(env.getDialogId()) {
                            case 26:
                                if (var == 2) return sendQuestDialog(env, 1693);
                            case 10002:
                                if (var == 2) {
                                    qs.setQuestVarById(0, var + 1);
                                    updateQuestStatus(env);
                                    PacketSendUtility.sendPacket(player, new SM_DIALOG_WINDOW(env.getVisibleObject().getObjectId(), 10));
                                    return true;
                                }
                        }
                    }
                    break;
                case 215045:
                    {
                        qs.setStatus(QuestStatus.REWARD);
                        qs.setQuestVarById(0, var + 2);
                        updateQuestStatus(env);
                        PacketSendUtility.sendPacket(player, new SM_DIALOG_WINDOW(env.getVisibleObject().getObjectId(), 10));
                    }
                    break;
                case 278019:
                    {
                        switch(env.getDialogId()) {
                            case 26:
                                if (var == 1) return sendQuestDialog(env, 1352);
                            case 10001:
                                if (var == 1) {
                                    qs.setQuestVarById(0, var + 1);
                                    updateQuestStatus(env);
                                    PacketSendUtility.sendPacket(player, new SM_DIALOG_WINDOW(env.getVisibleObject().getObjectId(), 10));
                                    return true;
                                }
                        }
                    }
            }
        } else if (qs.getStatus() == QuestStatus.REWARD) {
            if (targetId == 278019) {
                if (env.getDialogId() == -1) return sendQuestDialog(env, 10002); else if (env.getDialogId() == 1009) return sendQuestDialog(env, 5); else return defaultQuestEndDialog(env);
            }
            return false;
        }
        return false;
    }

    @Override
    public boolean onLvlUpEvent(QuestCookie env) {
        return defaultQuestOnLvlUpEvent(env, 2701);
    }
}
