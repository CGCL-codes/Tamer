package opengl;

public class Particle {

    private boolean active;

    private float life;

    private float fadeSpeed;

    private float r, g, b;

    private float x, y, z;

    private float xi, yi, zi;

    private float xg, yg, zg;

    public boolean isActive() {
        return active;
    }

    public void setActive(boolean active) {
        this.active = active;
    }

    public float getLife() {
        return life;
    }

    public void setLife(float life) {
        this.life = life;
    }

    public float getFadeSpeed() {
        return fadeSpeed;
    }

    public void setFadeSpeed(float fadeSpeed) {
        this.fadeSpeed = fadeSpeed;
    }

    public float getR() {
        return r;
    }

    public void setR(float r) {
        this.r = r;
    }

    public float getG() {
        return g;
    }

    public void setG(float g) {
        this.g = g;
    }

    public float getB() {
        return b;
    }

    public void setB(float b) {
        this.b = b;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }

    public float getZ() {
        return z;
    }

    public void setZ(float z) {
        this.z = z;
    }

    public float getXi() {
        return xi;
    }

    public void setXi(float xi) {
        this.xi = xi;
    }

    public float getYi() {
        return yi;
    }

    public void setYi(float yi) {
        this.yi = yi;
    }

    public float getZi() {
        return zi;
    }

    public void setZi(float zi) {
        this.zi = zi;
    }

    public float getXg() {
        return xg;
    }

    public void setXg(float xg) {
        this.xg = xg;
    }

    public float getYg() {
        return yg;
    }

    public void setYg(float yg) {
        this.yg = yg;
    }

    public float getZg() {
        return zg;
    }

    public void setZg(float zg) {
        this.zg = zg;
    }
}
