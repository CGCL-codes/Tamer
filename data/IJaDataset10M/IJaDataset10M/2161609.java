package algutil.fichier.exception;

public class SuppressionException extends Exception {

    public SuppressionException() {
        super();
    }

    public SuppressionException(String arg0) {
        super(arg0);
    }

    public SuppressionException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public SuppressionException(Throwable arg0) {
        super(arg0);
    }
}
