package net.exclaimindustries.fotobilder;

/**
 *<p>
 * <code>LoginResults</code> stores all the data picked up by <code>FBLoginConnection</code>
 * in a way that sure beats grabbing every variable from the finished connection
 * one at a time.
 *</p>
 *
 *<p>
 * Really, it's a glorified C-style struct.  With accessors.
 *</p>
 *
 * @author Nicholas Killewald
 */
public class LoginResults {

    private long quotatotal;

    private long quotaused;

    private long quotaremaining;

    private String message;

    private String servertime;

    private String rawxml;

    /** Creates a new instance of LoginResults */
    public LoginResults() {
        setQuotaTotal(-1);
        setQuotaUsed(-1);
        setQuotaRemaining(-1);
        setMessage("");
        setServerTime("");
    }

    public void setQuotaTotal(long quotatotal) {
        this.quotatotal = quotatotal;
    }

    public void setQuotaUsed(long quotaused) {
        this.quotaused = quotaused;
    }

    public void setQuotaRemaining(long quotaremaining) {
        this.quotaremaining = quotaremaining;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public void setServerTime(String servertime) {
        this.servertime = servertime;
    }

    /**
     * Returns the server message, if any.  Note this may be null or an empty
     * string.
     *
     * @return the server message, or null if there is none
     */
    public String getMessage() {
        return message;
    }

    /**
     * Returns the total quota, if the server reports it.  If it doesn't, this
     * will return -1.
     *
     * @return the total quota or -1 if none was given
     */
    public long getQuotaTotal() {
        return quotatotal;
    }

    /**
     * Returns the quota used, if the server reports it.  If it doesn't, this
     * will return -1.
     *
     * @return the quota used or -1 if none was given
     */
    public long getQuotaUsed() {
        return quotaused;
    }

    /**
     * Returns the quota remaining, if the server reports it.  If it doesn't, this
     * will return -1.
     *
     * @return the quota remaining or -1 if none was given
     */
    public long getQuotaRemaining() {
        return quotaremaining;
    }

    /**
     * Returns the ServerTime value as given by the server.  Note that this will
     * be in String format.  Also note that this may not even be defined or may
     * be an empty string if the server did not report the time.
     *
     * @return the ServerTime value, if any
     */
    public String getServerTime() {
        return servertime;
    }

    public void setRawXML(String rawxml) {
        this.rawxml = rawxml;
    }

    /**
     * Returns the raw XML output from the login attempt.  Because Login is so
     * extensible and has no real concrete specification, anything can happen,
     * so the ability to pull the raw XML is needed for full compliance.
     *
     * @return the XML generated by the login attempt
     */
    public String getRawXML() {
        return rawxml;
    }
}
