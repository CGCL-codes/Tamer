    private void bokActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bokActionPerformed
        // Add your handling code here:
        if(this.bibid.equals("4"))
        {
            if(tableSerial.getSelectedRow()!=-1)
            {
                this.setVolumeSerialId(dtmSerial.getValueAt(tableSerial.getSelectedRow(), 0).toString());
                this.dispose();
                this.setRetcode(0);
            }
        }
        else
        {
            if(tableNonSerial.getSelectedRow()!=-1)
            {
                this.setVolumeSerialId(dtmNonSerial.getValueAt(tableNonSerial.getSelectedRow(), 0).toString());
                this.dispose();
                this.setRetcode(0);
            }            
        }
    }//GEN-LAST:event_bokActionPerformed
    
