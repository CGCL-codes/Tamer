    public void setTableData(String bibId, java.util.Hashtable ht) {
        this.bibid=bibId;
        if(bibId.equals("4")) {
            java.awt.CardLayout cl = (java.awt.CardLayout)jPanel1.getLayout();
            cl.show(jPanel1, "serial");
            
            java.util.Enumeration enum = ht.keys();
            while(enum.hasMoreElements()) {
                String key = enum.nextElement().toString();
                java.util.Vector v1 = (java.util.Vector)ht.get(key);
                
                Object[] r = new Object[4];
                r[0]=key;
                r[1]=v1.elementAt(0);
                r[2]=v1.elementAt(1);
                r[3]=v1.elementAt(2);
                dtmSerial.addRow(r);
            }
        }
        else {
            java.awt.CardLayout cl = (java.awt.CardLayout)jPanel1.getLayout();
            cl.show(jPanel1, "nonserial");
            
            java.util.Enumeration enum = ht.keys();
            while(enum.hasMoreElements()) {
                String key = enum.nextElement().toString();
                java.util.Vector v1 = (java.util.Vector)ht.get(key);
                
                Object[] r = new Object[3];
                r[0]=key;
                r[1]=v1.elementAt(0);
                r[2]=v1.elementAt(1);
                dtmNonSerial.addRow(r);
            }
        }
    }
