package com.ibm.richtext.textpanel;

final class ThaiKeyRemap extends KeyRemap {

    public char remap(char c) {
        switch(c) {
            case '`':
                return '_';
            case '~':
                return '%';
            case '1':
                return 'ๅ';
            case '!':
                return '+';
            case '2':
                return '/';
            case '@':
                return '๑';
            case '3':
                return '-';
            case '#':
                return '๒';
            case '4':
                return 'ภ';
            case '$':
                return '๓';
            case '5':
                return 'ถ';
            case '%':
                return '๔';
            case '6':
                return 'ุ';
            case '^':
                return 'ู';
            case '7':
                return 'ึ';
            case '&':
                return '฿';
            case '8':
                return 'ค';
            case '*':
                return '๕';
            case '9':
                return 'ต';
            case '(':
                return '๖';
            case '0':
                return 'จ';
            case ')':
                return '๗';
            case '-':
                return 'ข';
            case '_':
                return '๘';
            case '=':
                return 'จ';
            case '+':
                return '๙';
            case 'q':
                return 'ๆ';
            case 'Q':
                return '๐';
            case 'w':
                return 'ไ';
            case 'W':
                return '"';
            case 'e':
                return 'ำ';
            case 'E':
                return 'ฎ';
            case 'r':
                return 'พ';
            case 'R':
                return 'ฑ';
            case 't':
                return 'ะ';
            case 'T':
                return 'ธ';
            case 'y':
                return 'ั';
            case 'Y':
                return 'ํ';
            case 'u':
                return 'ี';
            case 'U':
                return '๊';
            case 'i':
                return 'ร';
            case 'I':
                return 'ณ';
            case 'o':
                return 'น';
            case 'O':
                return 'ฯ';
            case 'p':
                return 'ย';
            case 'P':
                return 'ญ';
            case '[':
                return 'บ';
            case '{':
                return 'ฐ';
            case ']':
                return 'ล';
            case '}':
                return ',';
            case '\\':
                return 'ฃ';
            case '|':
                return 'ฅ';
            case 'a':
                return 'ฟ';
            case 'A':
                return 'ฤ';
            case 's':
                return 'ห';
            case 'S':
                return 'ฆ';
            case 'd':
                return 'ก';
            case 'D':
                return 'ฏ';
            case 'f':
                return 'ด';
            case 'F':
                return 'โ';
            case 'g':
                return 'เ';
            case 'G':
                return 'ฌ';
            case 'h':
                return '้';
            case 'H':
                return '็';
            case 'j':
                return '่';
            case 'J':
                return '๋';
            case 'k':
                return 'า';
            case 'K':
                return 'ษ';
            case 'l':
                return 'ส';
            case 'L':
                return 'ศ';
            case ';':
                return 'ว';
            case ':':
                return 'ซ';
            case '\'':
                return 'ง';
            case '\"':
                return '.';
            case 'z':
                return 'ผ';
            case 'Z':
                return '(';
            case 'x':
                return 'ป';
            case 'X':
                return ')';
            case 'c':
                return 'แ';
            case 'C':
                return 'ฉ';
            case 'v':
                return 'อ';
            case 'V':
                return 'ฮ';
            case 'b':
                return 'ิ';
            case 'B':
                return 'ฺ';
            case 'n':
                return 'ื';
            case 'N':
                return '์';
            case 'm':
                return 'ท';
            case 'M':
                return '?';
            case ',':
                return 'ม';
            case '<':
                return 'ฒ';
            case '.':
                return 'ใ';
            case '>':
                return 'ฬ';
            case '/':
                return 'ฝ';
            case '?':
                return 'ฦ';
        }
        return c;
    }
}
