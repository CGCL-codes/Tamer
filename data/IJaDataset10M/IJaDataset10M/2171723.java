import com.planet_ink.coffee_mud.Races.interfaces.*;
import java.util.*;

@SuppressWarnings("unchecked")
public class GiantLizard extends StdRace {

    public String ID() {
        return "GiantLizard";
    }

    public String name() {
        return "Giant Lizard";
    }

    public int shortestMale() {
        return 70;
    }

    public int shortestFemale() {
        return 70;
    }

    public int heightVariance() {
        return 20;
    }

    public int lightestWeight() {
        return 2500;
    }

    public int weightVariance() {
        return 500;
    }

    public long forbiddenWornBits() {
        return Integer.MAX_VALUE - Wearable.WORN_EYES;
    }

    public String racialCategory() {
        return "Reptile";
    }

    private static final int[] parts = { 0, 2, 0, 1, 1, 0, 0, 1, 4, 4, 1, 0, 1, 1, 1, 0 };

    public int[] bodyMask() {
        return parts;
    }

    private int[] agingChart = { 0, 2, 4, 8, 14, 30, 40, 41, 42 };

    public int[] getAgingChart() {
        return agingChart;
    }

    protected static Vector resources = new Vector();

    public int availabilityCode() {
        return Area.THEME_FANTASY | Area.THEME_SKILLONLYMASK;
    }

    public void affectCharStats(MOB affectedMOB, CharStats affectableStats) {
        super.affectCharStats(affectedMOB, affectableStats);
        affectableStats.setRacialStat(CharStats.STAT_STRENGTH, 35);
        affectableStats.setRacialStat(CharStats.STAT_INTELLIGENCE, 1);
        affectableStats.setRacialStat(CharStats.STAT_DEXTERITY, 3);
    }

    public String arriveStr() {
        return "crawls in";
    }

    public String leaveStr() {
        return "crawls";
    }

    public Weapon myNaturalWeapon() {
        if (naturalWeapon == null) {
            naturalWeapon = CMClass.getWeapon("StdWeapon");
            naturalWeapon.setName("sharp claws");
            naturalWeapon.setWeaponType(Weapon.TYPE_SLASHING);
        }
        return naturalWeapon;
    }

    public String healthText(MOB viewer, MOB mob) {
        double pct = (CMath.div(mob.curState().getHitPoints(), mob.maxState().getHitPoints()));
        if (pct < .10) return "^r" + mob.displayName(viewer) + "^r is facing a cold death!^N"; else if (pct < .20) return "^r" + mob.displayName(viewer) + "^r is covered in blood.^N"; else if (pct < .30) return "^r" + mob.displayName(viewer) + "^r is bleeding badly from lots of wounds.^N"; else if (pct < .40) return "^y" + mob.displayName(viewer) + "^y has numerous bloody wounds and gashes.^N"; else if (pct < .50) return "^y" + mob.displayName(viewer) + "^y has some bloody wounds and gashes.^N"; else if (pct < .60) return "^p" + mob.displayName(viewer) + "^p has a few bloody wounds.^N"; else if (pct < .70) return "^p" + mob.displayName(viewer) + "^p is cut and bruised heavily.^N"; else if (pct < .80) return "^g" + mob.displayName(viewer) + "^g has some minor cuts and bruises.^N"; else if (pct < .90) return "^g" + mob.displayName(viewer) + "^g has a few bruises and scratched scales.^N"; else if (pct < .99) return "^g" + mob.displayName(viewer) + "^g has a few small bruises.^N"; else return "^c" + mob.displayName(viewer) + "^c is in perfect health.^N";
    }

    public Vector myResources() {
        synchronized (resources) {
            if (resources.size() == 0) {
                resources.addElement(makeResource("a " + name().toLowerCase() + " tongue", RawMaterial.RESOURCE_MEAT));
                for (int i = 0; i < 35; i++) resources.addElement(makeResource("a pound of " + name().toLowerCase() + " meat", RawMaterial.RESOURCE_MEAT));
                for (int i = 0; i < 85; i++) resources.addElement(makeResource("a " + name().toLowerCase() + " hide", RawMaterial.RESOURCE_SCALES));
                resources.addElement(makeResource("some " + name().toLowerCase() + " blood", RawMaterial.RESOURCE_BLOOD));
            }
        }
        return resources;
    }
}
