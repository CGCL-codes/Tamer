    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {
        flag = 1;
        placa = jTextField1.getText();
        marca = jTextField6.getText();
        modelo = jTextField2.getText();
        opcionais = jTextField3.getText();
        cor = jTextField7.getText();
        observacoes = jTextField4.getText();
        int temp = jComboBox1.getSelectedIndex();
        tipoVeiculo = persistencia.PersistenciaTipoVeiculo.tipoVeiculoList.get(temp).getIdTipoVeiculo();
        System.out.println(tipoVeiculo);
        if (placa.length() != 8) {
            flag = 0;
            JOptionPane.showMessageDialog(null, "Erro: Placa fornecida inválida");
        }
        if (marca.length() < 1) {
            flag = 0;
            JOptionPane.showMessageDialog(null, "Erro: Marca fornecida inválida");
        }
        if (modelo.length() < 1) {
            flag = 0;
            JOptionPane.showMessageDialog(null, "Erro: Modelo fornecido inválido");
        }
        if (flag == 1) {
            id = persistencia.PersistenciaVeiculo.geraIDVeiculo();
            dominio.Veiculo v = new dominio.Veiculo(placa, modelo, marca, id);
            v.setCor(cor);
            v.setObservacoes(observacoes);
            v.setOpcionais(opcionais);
            v.setTipo(tipoVeiculo);
            persistencia.PersistenciaVeiculo.gravarobjeto(v);
            SimpleDateFormat formatter = new SimpleDateFormat("HH:mm:ss");
            log.info("Veiculo " + placa + " cadastrado as " + formatter.format(new Date()));
            dispose();
        }
    }

