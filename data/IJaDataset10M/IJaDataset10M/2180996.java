package ch.mypics.wtt.generators;

/**
 * 
 * @author <a href="mailto:mhaessig@okta.ch">Mathias H&auml;ssig</a>
 */
public abstract class ClassGenerator extends AbstractGenerator {

    private String _package;

    public ClassGenerator(String path, String file, String pkg) {
        super(path, file, pkg);
        _package = pkg;
    }

    protected void generateHeader() {
        write("/*");
        write(" * Generated by WTT.");
        write(" * Do not edit! All changes will be lost!");
        write(" */");
        write("package " + _package + ";");
    }
}
