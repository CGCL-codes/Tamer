    private void excluirTButtonActionPerformed(java.awt.event.ActionEvent evt) {
        Map<String, String> entrada = new HashMap();
        entrada.put("codigo", codTText.getText());
        Map<String, String> saida = null;
        try {
            saida = Fachada.ComandoExcluiTurma(entrada);
        } catch (Exception ex) {
            Logger.getLogger(Exclui.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(rootPane, saida.get("msg"));
        codTText.setText("");
    }

    private void excluirAButtonActionPerformed(java.awt.event.ActionEvent evt) {
        Map<String, String> entrada = new HashMap();
        entrada.put("codigo", codFText.getText());
        entrada.put("codTurma", codTurmaText.getText());
        Map<String, String> saida = new HashMap();
        try {
            saida = Fachada.ComandoExcluiAluno(entrada);
        } catch (Exception ex) {
            Logger.getLogger(Exclui.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(rootPane, saida.get("msg"));
        codAText.setText("");
        codTurmaAText.setText("");
    }

    private void excluirFButtonActionPerformed(java.awt.event.ActionEvent evt) {
        Map<String, String> saida = new HashMap();
        if (dirRadioButton.isSelected()) {
            try {
                saida = Fachada.ComandoExcluiDiretor(null);
            } catch (Exception ex) {
                Logger.getLogger(Exclui.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(rootPane, saida.get("msg"));
        } else if (profRadioButton.isSelected()) {
            Map<String, String> entrada = new HashMap();
            entrada.put("codigo", codFText.getText());
            entrada.put("codTurma", codTurmaText.getText());
            try {
                saida = Fachada.ComandoExcluiProfessor(entrada);
            } catch (Exception ex) {
                Logger.getLogger(Exclui.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(rootPane, saida.get("msg"));
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione um tipo de funcionário!");
        }
        buttonGroup1.clearSelection();
        codFText.setText("");
        codTurmaText.setText("");
    }

    private void dirRadioButtonActionPerformed(java.awt.event.ActionEvent evt) {
        codFLabel.setVisible(false);
        codFText.setVisible(false);
        codTurmaLabel.setVisible(false);
        codTurmaText.setVisible(false);
    }

    private void profRadioButtonActionPerformed(java.awt.event.ActionEvent evt) {
        codFLabel.setVisible(true);
        codFText.setVisible(true);
    }

