package org.proteored.miapeapi.xml.pride.msi;

import java.util.HashSet;
import java.util.Set;
import org.proteored.miapeapi.interfaces.msi.InputData;
import org.proteored.miapeapi.interfaces.msi.InputDataSet;
import org.proteored.miapeapi.xml.pride.autogenerated.ExperimentType.MzData;
import org.proteored.miapeapi.xml.util.MiapeXmlUtil;

public class InputDataSetImpl implements InputDataSet {

    private final MzData mzData;

    private final Integer identifier;

    public InputDataSetImpl(MzData mzData, Integer identifier) {
        this.mzData = mzData;
        this.identifier = identifier;
    }

    @Override
    public int getId() {
        return identifier;
    }

    @Override
    public Set<InputData> getInputDatas() {
        if (mzData != null) {
            Set<InputData> inputDatas = new HashSet<InputData>();
            inputDatas.add(new InputDataImpl(mzData, MiapeXmlUtil.InputDataCounter.increaseCounter()));
            return inputDatas;
        }
        return null;
    }

    @Override
    public String getName() {
        return mzData.getAccessionNumber();
    }
}
