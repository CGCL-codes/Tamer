package com.habitsoft.kiyaa.util;

public class CurrencyParseException extends Exception {

    public CurrencyParseException() {
        super();
    }

    public CurrencyParseException(String message, Throwable cause) {
        super(message, cause);
    }

    public CurrencyParseException(String message) {
        super(message);
    }

    public CurrencyParseException(Throwable cause) {
        super(cause);
    }
}
