package org.proteored.miapeapi.xml.ms;

import org.proteored.miapeapi.interfaces.Equipment;
import org.proteored.miapeapi.xml.ms.autogenerated.MIAPEEquipmentType;
import org.proteored.miapeapi.xml.ms.util.MsControlVocabularyXmlFactory;

public class MiapeEquipmentImpl implements Equipment {

    private final MIAPEEquipmentType miapeEquipment;

    public MiapeEquipmentImpl(MIAPEEquipmentType miapeEquipment) {
        this.miapeEquipment = miapeEquipment;
    }

    @Override
    public String getCatalogNumber() {
        if (miapeEquipment == null) return null;
        return this.miapeEquipment.getCatalogNumber();
    }

    @Override
    public String getComments() {
        if (miapeEquipment == null) return null;
        return this.miapeEquipment.getComments();
    }

    @Override
    public String getDescription() {
        if (miapeEquipment == null) return null;
        return this.miapeEquipment.getDescription();
    }

    @Override
    public String getManufacturer() {
        if (miapeEquipment == null) return null;
        return MsControlVocabularyXmlFactory.getName(this.miapeEquipment.getManufacturer());
    }

    @Override
    public String getModel() {
        if (miapeEquipment == null) return null;
        return MsControlVocabularyXmlFactory.getName(miapeEquipment.getModel());
    }

    @Override
    public String getName() {
        if (miapeEquipment == null) return null;
        return MsControlVocabularyXmlFactory.getName(miapeEquipment.getName());
    }

    @Override
    public String getParameters() {
        return this.miapeEquipment.getParameters();
    }

    @Override
    public String getUri() {
        return this.miapeEquipment.getURI();
    }

    @Override
    public String getVersion() {
        return this.miapeEquipment.getVersion();
    }

    @Override
    public int getId() {
        return -1;
    }
}
