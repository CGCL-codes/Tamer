    private void rPrueba_ButtonActionPerformed(java.awt.event.ActionEvent evt) {
        int indice = jTable1.getSelectedRow();
        if (indice < 0) {
            controlador.mostrarError("No se ha seleccionado una prueba de la lista mostrada");
        } else {
            String tipo = ModeloTable1.getValueAt(indice, 0).toString();
            if (tipo == "Analisis") {
                ((CardLayout) this.getParent().getLayout()).show(this.getParent(), "analisis");
                controlador.verAnalisis(indice);
            } else if (tipo == "Radiografia" || tipo == "Resonancia") {
                ((CardLayout) this.getParent().getLayout()).show(this.getParent(), "radiografias");
                controlador.verRadiografia(indice);
            }
        }
    }

    private void rReceta_ButtonActionPerformed(java.awt.event.ActionEvent evt) {
        int indice = jTable2.getSelectedRow();
        if (indice < 0) {
            controlador.mostrarError("No se ha seleccionado una receta de la lista mostrada");
        } else {
            controlador.verReceta(indice);
            ((CardLayout) this.getParent().getLayout()).show(this.getParent(), "receta");
        }
    }

