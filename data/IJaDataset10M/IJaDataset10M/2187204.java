    public void mostrarEntradaHistorial(EntradaHistorial entrada) {
        String[] etiquetas = new String[] { "Prueba", "Fecha" };
        ModeloTable1 = new ModeloJTable(etiquetas);
        jTable1.setModel(ModeloTable1);
        etiquetas = new String[] { "Fecha", "Primer Medicamento" };
        ModeloTable2 = new ModeloJTable(etiquetas);
        jTable2.setModel(ModeloTable2);
        etiquetas = new String[] { "Nombre" };
        ModeloTable3 = new ModeloJTable(etiquetas);
        jTable3.setModel(ModeloTable3);
        if (entrada != null) {
            String obs = "";
            for (int j = 0; j < entrada.tam_observaciones(); ++j) {
                obs = obs + " \n" + entrada.getObservaciones().get(j).getComentario();
            }
            jTextArea1.setText(obs);
            jTextField2.setText(entrada.getFechaInicio());
            jTextField1.setText(controlador.getNombreTratamiento());
            for (int j = 0; j < entrada.num_analisis(); j++) {
                Object nuevo[] = { "Analisis", entrada.getFechaInicio() };
                ModeloTable1.addRow(nuevo);
            }
            for (int j = 0; j < entrada.num_radiografias(); j++) {
                Object nuevo[] = { "Radiografia", entrada.getFechaInicio() };
                ModeloTable1.addRow(nuevo);
            }
            for (int j = 0; j < entrada.tam_recetas(); j++) {
                String m = entrada.getReceta(j).getFecha();
                Object nuevo[] = { m, entrada.getReceta(j).getMedicamentos().get(0).getMedicamento().getName() };
                ModeloTable2.addRow(nuevo);
            }
            for (int j = 0; j < entrada.num_analisis(); j++) {
                Object nuevo[] = { entrada.getAnalisis(j).getMedico() };
                ModeloTable3.addRow(nuevo);
            }
            for (int j = 0; j < entrada.num_radiografias(); j++) {
                Object nuevo[] = { entrada.getRadiografia(j).getMedico() };
                ModeloTable3.addRow(nuevo);
            }
        }
    }

