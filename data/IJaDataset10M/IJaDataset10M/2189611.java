package net.sf.metarbe;

@SuppressWarnings("serial")
public class RuleSessionException extends Exception {

    public RuleSessionException() {
    }

    public RuleSessionException(String arg0) {
        super(arg0);
    }

    public RuleSessionException(Throwable arg0) {
        super(arg0);
    }

    public RuleSessionException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }
}
