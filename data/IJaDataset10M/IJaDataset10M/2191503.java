    private void btnReporteVentasActionPerformed(java.awt.event.ActionEvent evt) {
        ReporteVentaDataSource datasource = new ReporteVentaDataSource();
        List<Object> objects = gestorComprobante.reporteVentas();
        for (int i = 0; i < objects.size(); i++) {
            Object[] os = (Object[]) objects.get(i);
            ResultadoVentaDTO resultadoVentaDTO = new ResultadoVentaDTO(Integer.parseInt(os[0].toString()), Integer.parseInt(os[1].toString()), Float.parseFloat(os[2].toString()), Float.parseFloat(os[3].toString()), Float.parseFloat(os[4].toString()), Float.parseFloat(os[5].toString()), Float.parseFloat(os[6].toString()));
            datasource.addResultado(resultadoVentaDTO);
        }
        try {
            JasperReport reporte = (JasperReport) JRLoader.loadObject("ReporteVentas.jasper");
            JasperPrint jasperPrint = JasperFillManager.fillReport(reporte, null, datasource);
            String home = System.getProperties().getProperty("user.home");
            Calendar actual = Calendar.getInstance();
            JasperExportManager.exportReportToPdfFile(jasperPrint, home + "/Escritorio/ReporteVentas_" + actual.get(Calendar.DATE) + "_" + (actual.get(Calendar.MONTH) + 1) + ".pdf");
            JasperViewer.viewReport(jasperPrint, false);
        } catch (Exception e) {
            System.out.print(e.getMessage());
        }
    }

