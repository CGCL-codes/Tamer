package net.kortsoft.slparser.util;

public class SLBuildException extends Exception {

    public SLBuildException() {
        super();
    }

    public SLBuildException(String message, Throwable cause) {
        super(message, cause);
    }

    public SLBuildException(String message) {
        super(message);
    }

    public SLBuildException(Throwable cause) {
        super(cause);
    }

    private static final long serialVersionUID = -3915489040271168037L;
}
