package net.mlw.data;

public class Team {

    private String id;

    private String name;

    /**
   * @return Returns the id.
   */
    public String getId() {
        return id;
    }

    /**
   * @param id The id to set.
   */
    public void setId(String id) {
        this.id = id;
    }

    /**
   * @return Returns the name.
   */
    public String getName() {
        return name;
    }

    /**
   * @param name The name to set.
   */
    public void setName(String name) {
        this.name = name;
    }
}
