package net.sourceforge.ivi.waveview.ui;

import org.eclipse.swt.graphics.Color;
import org.eclipse.swt.graphics.FontMetrics;
import org.eclipse.swt.graphics.Rectangle;

public interface IWaveGC {

    /****************************************************************
	 * drawLine()
	 ****************************************************************/
    void drawLine(int x1, int y1, int x2, int y2);

    /****************************************************************
	 * drawOval()
	 ****************************************************************/
    void drawOval(int x, int y, int w, int h);

    /****************************************************************
	 * drawPoint()
	 ****************************************************************/
    void drawPoint(int x, int y);

    /****************************************************************
	 * drawPolygon()
	 ****************************************************************/
    void drawPolygon(int points[]);

    /****************************************************************
	 * drawRectangle()
	 ****************************************************************/
    void drawRectangle(int x, int y, int w, int h);

    /****************************************************************
	 * drawRectangle()
	 ****************************************************************/
    void drawRectangle(Rectangle r);

    /****************************************************************
	 * drawRoundRectangle()
	 ****************************************************************/
    void drawRoundRectangle(int x, int y, int w, int h, int arcw, int argh);

    /****************************************************************
	 * setForeground()
	 ****************************************************************/
    void setForeground(Color c);

    /****************************************************************
	 * setBackground()
	 ****************************************************************/
    void setBackground(Color c);

    /****************************************************************
	 * drawString()
	 ****************************************************************/
    void drawString(String str, int x, int y);

    /****************************************************************
	 * drawString()
	 ****************************************************************/
    void drawString(String str, int x, int y, boolean isTransparent);

    /****************************************************************
	 * fillRectangle()
	 ****************************************************************/
    void fillRectangle(int x, int y, int width, int height);

    /****************************************************************
	 * getFontMetrics()
	 ****************************************************************/
    FontMetrics getFontMetrics();
}
