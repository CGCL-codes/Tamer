    /**
     * Called when user selects a font.  Sets the preview font to selected.
     *
     * @param e an <code>ActionEvent</code> value
     */
    public void actionPerformed(ActionEvent e) {
        Font f = getSelectedFont();
        jLabel5.setFont(f);
    }

    /**
     * Describe <code>setThemeFont</code> method here.
     *
     * @param f a <code>Font</code> value
     */
    public void setThemeFont(Font f) {
        int tsz, tst;
        jList2.setSelectedItem(f.getName());
        jComboBox1.setSelectedItem(new Integer(f.getSize()));
        tst = Font.PLAIN;
        if (f.isBold()) jCheckBox1.setSelected(true);
        if (f.isItalic()) jCheckBox2.setSelected(true);
    }

    /**
     * Describe <code>getSelectedFont</code> method here.
     *
     * @return a <code>Font</code> value
     */
    public Font getSelectedFont() {
        int style = Font.PLAIN;
        if (jCheckBox1.isSelected()) style += Font.BOLD;
        if (jCheckBox2.isSelected()) style += Font.ITALIC;
        String fontName = jList2.getSelectedItem().toString();
        String fontSizeS = jComboBox1.getSelectedItem().toString();
        int fontSize = Integer.parseInt(fontSizeS);
        return new Font(fontName, style, fontSize);
    }

