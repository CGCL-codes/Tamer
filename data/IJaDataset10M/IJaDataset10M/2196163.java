package dk.syscall.yamsie.moviesheet.tvixie;

public class RectangleShapeElement extends ShapeElement {

    private String fill;

    private String roundness;

    private String corners;

    public String getFill() {
        return fill;
    }

    public void setFill(String fill) {
        this.fill = fill;
    }

    public String getRoundness() {
        return roundness;
    }

    public void setRoundness(String roundness) {
        this.roundness = roundness;
    }

    public String getCorners() {
        return corners;
    }

    public void setCorners(String corners) {
        this.corners = corners;
    }
}
