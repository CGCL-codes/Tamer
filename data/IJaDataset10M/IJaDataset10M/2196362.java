package com.agentfactory.vacworld.vacGui;

/**
 * Simple wrapper class to encapsulate a square which has been perceived
 * by a Movable object with direction.
 */
public class PerceptSquare {

    private Square square;

    private RelativeDirection relativeDirection;

    public PerceptSquare(Square square, RelativeDirection relativeDirection) {
        this.square = square;
        this.relativeDirection = relativeDirection;
    }

    public Square getSquare() {
        return square;
    }

    public RelativeDirection getRelativeDirection() {
        return relativeDirection;
    }
}
