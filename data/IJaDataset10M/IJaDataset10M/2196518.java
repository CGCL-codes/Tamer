public class ServicioRegistro extends HttpServlet {

    /** 
     * Processes requests for both HTTP <code>GET</code> and <code>POST</code> methods.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    protected void processRequest(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
        response.setContentType("text/html;charset=UTF-8");
        PrintWriter out = response.getWriter();
        try {
            GestorUsuarios gestor = new GestorUsuarios(getServletContext().getInitParameter("URL_servidor"));
            String id_user = request.getParameter("login");
            String nombre = request.getParameter("nombre");
            String password = request.getParameter("clave");
            String tarj = request.getParameter("tarjeta");
            String pc = request.getParameter("claveC");
            if (id_user != null && nombre != null && tarj != null && password != null && password.matches(pc)) {
                Cliente user = new Cliente(nombre, id_user, password, tarj);
                if (gestor.agregar(user) == false) {
                    request.getRequestDispatcher("errorRegistro.jsp?errorType=2").forward(request, response);
                } else {
                    request.getRequestDispatcher("login.jsp").forward(request, response);
                }
            } else {
                request.getRequestDispatcher("errorRegistro.jsp?errorType=1").forward(request, response);
            }
        } catch (Exception e) {
        } finally {
            out.close();
        }
    }

    /** 
     * Handles the HTTP <code>GET</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
        processRequest(request, response);
    }

    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
        processRequest(request, response);
    }

    /** 
     * Returns a short description of the servlet.
     * @return a String containing servlet description
     */
    @Override
    public String getServletInfo() {
        return "Short description";
    }
}
