    private boolean addedFirst = false;

    public void append(final String s) {
        GUIHelpers.invokeLater(new Runnable() {

            public void run() {
                if (addedFirst) txtMessage.append("\n"); else addedFirst = true;
                txtMessage.append(s);
                if (chkAutoscroll.isSelected()) {
                    try {
                        Rectangle view = txtMessage.modelToView(txtMessage.getCaretPosition());
                        if (view != null) {
                            view.setRect(view.getX(), view.getY() + view.getHeight(), view.getWidth(), view.getHeight());
                            txtMessage.scrollRectToVisible(view);
                        }
                    } catch (BadLocationException e) {
                    }
                }
            }
        });
    }

