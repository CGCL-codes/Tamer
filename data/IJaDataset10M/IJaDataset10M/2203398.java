    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {
        ArrayList<String> retorno = new ArrayList();
        int i = jTable1.getSelectedRow();
        try {
            for (int j = 0; j < meta.getColumnCount(); j++) {
                retorno.add(resultados.get(i)[j]);
            }
        } catch (SQLException ex) {
            Logger.getLogger(BuscaExibicaoJornal.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            if (JOptionPane.showConfirmDialog(null, "Deseja realmente remover a Exibição de Jornal com ID Jornal = " + retorno.get(0) + " do dia " + retorno.get(4) + "?", "Confirmação de Remoção", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                banco.update("DELETE FROM exibicaoJornal WHERE idPr = " + retorno.get(0) + " AND idFu = " + retorno.get(2) + " AND dataExJo = TO_DATE('" + retorno.get(4) + "','DD/MM/YYYY') AND horaInicioExJo = '" + retorno.get(5) + "'");
                JOptionPane.showMessageDialog(null, "Exibição de Jornal removida com sucesso!", "Remoção bem Sucedida", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception ex) {
            Logger.getLogger(BuscaExibicaoJornal.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Ocorreu um erro na remoção da Exibição de Jornal.", "Erro de Remoção", JOptionPane.ERROR_MESSAGE);
        }
    }

