    public BuscaExibicaoJornal(Connect banco) {
        initComponents();
        this.banco = banco;
        resultados = new ArrayList();
        try {
            ResultSet rs = this.banco.query("SELECT exibicaoJornal.idPr AS \"ID do Jornal\", jornal.tituloPr AS \"Nome do Jornal\", exibicaoJornal.idFu AS \"ID do Apresentador\", funcionario.nomeCompletoFu AS \"Nome do Apresentador\", TO_CHAR(exibicaoJornal.dataExJo,'DD/MM/YYYY') AS \"Data de Exibição\", exibicaoJornal.horaInicioExJo AS \"Horário de Início\", exibicaoJornal.horaFimExJo AS \"Horário de Fim\", exibicaoJornal.ibopeExJo AS \"IBOPE\" FROM ((exibicaoJornal JOIN jornal ON exibicaoJornal.idPr = jornal.idPr) JOIN funcionario ON exibicaoJornal.idFu = funcionario.idFu)");
            meta = rs.getMetaData();
            String[] columns = new String[meta.getColumnCount()];
            for (int i = 1; i <= meta.getColumnCount(); i++) {
                columns[i - 1] = meta.getColumnName(i);
            }
            while (rs.next()) {
                String[] row = new String[meta.getColumnCount()];
                for (int i = 1; i <= meta.getColumnCount(); i++) {
                    row[i - 1] = rs.getString(i);
                }
                resultados.add(row);
            }
            String[][] dados = new String[resultados.size()][meta.getColumnCount()];
            for (int i = 0; i < resultados.size(); i++) {
                dados[i] = resultados.get(i);
            }
            jTable1.setModel(new DefaultTableModel(dados, columns));
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Ocorreu algum problema ao buscar. Confira o formato dos parâmetros", "Erro ao Buscar", JOptionPane.ERROR_MESSAGE);
        }
    }

