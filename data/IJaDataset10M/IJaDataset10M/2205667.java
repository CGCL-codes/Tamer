package jw.bznetwork.client;

public class ShowMessageException extends Exception {

    public ShowMessageException() {
        super();
    }

    public ShowMessageException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public ShowMessageException(String arg0) {
        super(arg0);
    }

    public ShowMessageException(Throwable arg0) {
        super(arg0);
    }
}
