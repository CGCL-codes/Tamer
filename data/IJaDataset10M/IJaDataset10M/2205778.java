    private void tblListaOcupacionMouseClicked(java.awt.event.MouseEvent evt) {
        int rows = tblListaOcupacion.getRowCount();
        if (rows > 0) {
            int rowSel = tblListaOcupacion.getSelectedRow();
            Object col1 = tblListaOcupacion.getValueAt(rowSel, 0);
            Object col2 = tblListaOcupacion.getValueAt(rowSel, 1);
            String idTipoTrab = String.valueOf(col1);
            String codigotiptrab = String.valueOf(col2);
            GUIDatoslaborales.txtIdTipPago.setText(idTipoTrab);
            GUIDatoslaborales.txtDescTipPago.setText(codigotiptrab);
            dispose();
        }
    }

