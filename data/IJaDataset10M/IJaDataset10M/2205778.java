    private void txtbuscarxdescKeyTyped(java.awt.event.KeyEvent evt) {
        TableRowSorter<TableModel> sorter = new TableRowSorter<TableModel>(tblListaOcupacion.getModel());
        tblListaOcupacion.setRowSorter(sorter);
        String text = txtbuscarxdesc.getText().toUpperCase();
        if (text.length() == 0) {
            sorter.setRowFilter(null);
        } else {
            sorter.setRowFilter(RowFilter.regexFilter("^" + text, 1));
        }
    }

