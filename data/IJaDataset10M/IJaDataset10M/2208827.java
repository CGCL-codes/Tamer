    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {
        if (!txtTipoLog.getText().isEmpty()) {
            TipoLogradouro tipoLog = new TipoLogradouro();
            tipoLog.setTpl_desc(txtTipoLog.getText().toUpperCase());
            try {
                TipoLogradouroDao tipoLogDao = new TipoLogradouroDao();
                tipoLogDao.incluir(tipoLog);
            } catch (Exception ex) {
                Logger.getLogger(CadastraTipoLog.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "Dados Gravados com Sucesso!", "Mensagem", JOptionPane.INFORMATION_MESSAGE);
            dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Campo Tipo Logradouro Vazio!");
        }
    }

