    private void btnNovoActionPerformed(java.awt.event.ActionEvent evt) {
        if (FormPrincipal.formPessoa.isVisible()) {
            Popup popup = Popup.obterInstancia();
            popup.exibirMensagem("Feche a janela '" + FormPrincipal.formPessoa.getTitle() + "' ativa");
        } else {
            FormPrincipal.formPessoa.setFunction(Form.FUNCTION_INSERT);
            FormPrincipal.formPessoa.limparCampos();
            FormPrincipal.formPessoa.setTitle("Inserir Pessoa");
            FormPrincipal.formPessoa.setVisible(true);
        }
    }

    private void btnLimparActionPerformed(java.awt.event.ActionEvent evt) {
        validaCodigo.setText(null);
        this.codigo.setText(null);
        this.nome.setText(null);
        autalizarTabela();
    }

    private void btnFiltrarActionPerformed(java.awt.event.ActionEvent evt) {
        validaCodigo.setText(null);
        if (Validacao.validarInteiro(codigo.getText(), false)) {
            Pessoa filtro = new Pessoa();
            if (codigo.getText().length() > 0) {
                filtro.setPersonId(Integer.parseInt(codigo.getText()));
            }
            filtro.setName(nome.getText());
            autalizarTabela(filtro);
        } else {
            validaCodigo.setText("C칩digo inv치lido");
        }
    }

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {
        Popup mensagem = Popup.obterInstancia();
        if (FormPrincipal.formPessoa.isVisible()) {
            Popup popup = Popup.obterInstancia();
            popup.exibirMensagem("Feche a janela '" + FormPrincipal.formPessoa.getTitle() + "' ativa");
        } else {
            if (tablePessoas.getSelectedRowCount() > 0) {
                if (tablePessoas.getSelectedRowCount() > 1) {
                    mensagem.exibirMensagem("Selecione apenas um registro!");
                } else {
                    FormPrincipal.formPessoa.setFunction(Form.FUNCTION_UPDATE);
                    FormPrincipal.formPessoa.popularCampos(Integer.parseInt(String.valueOf(tablePessoas.getValueAt(tablePessoas.getSelectedRow(), 0))));
                    FormPrincipal.formPessoa.setTitle("Editar Pessoa");
                    FormPrincipal.formPessoa.setVisible(true);
                }
            } else {
                mensagem.exibirMensagem("Nenhuma registro selecionado!");
            }
        }
    }

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {
        Popup mensagem = Popup.obterInstancia();
        if (tablePessoas.getSelectedRowCount() > 0) {
            if (mensagem.pedirConfirmacao("Tem certeza que deseja excluir o(s) registro selecionado?")) {
                int selecionados[] = tablePessoas.getSelectedRows();
                for (int i = 0; i < selecionados.length; i++) {
                    Pessoa pessoa = new Pessoa();
                    pessoa.setPersonId(Integer.parseInt(String.valueOf(tablePessoas.getValueAt(selecionados[i], 0))));
                    ControlaPessoa ctlPessoa = new ControlaPessoa(pessoa, null);
                    ctlPessoa.excluirPessoa();
                    FormPrincipal.formPessoaBusca.autalizarTabela();
                }
            }
        } else {
            mensagem.exibirMensagem("Nenhuma registro selecionado!");
        }
    }

    private void codigoKeyPressed(java.awt.event.KeyEvent evt) {
        String tecla = KeyEvent.getKeyText(evt.getKeyCode());
        if (tecla.equalsIgnoreCase("Enter")) {
            this.btnFiltrarActionPerformed(null);
        }
    }

    private void nomeKeyPressed(java.awt.event.KeyEvent evt) {
        String tecla = KeyEvent.getKeyText(evt.getKeyCode());
        if (tecla.equalsIgnoreCase("Enter")) {
            this.btnFiltrarActionPerformed(null);
        }
    }

