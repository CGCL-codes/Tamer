package com.ivis.xprocess.framework.vcs.exceptions;

public class VCSMergeException extends VCSException {

    private static final long serialVersionUID = 1L;

    public VCSMergeException() {
        super();
    }

    public VCSMergeException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public VCSMergeException(String arg0) {
        super(arg0);
    }

    public VCSMergeException(Throwable arg0) {
        super(arg0);
    }
}
