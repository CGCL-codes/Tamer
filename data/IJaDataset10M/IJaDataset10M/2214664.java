package thegamezoo;

/**
 * The Interface IAquarium.
 */
public interface IAquarium {

    /**
	 * Gets the salinity.
	 *
	 * @return the salinity
	 */
    public int getSalinity();

    /**
	 * Sets the salinity.
	 *
	 * @param salinity the new salinity
	 */
    public void setSalinity(int salinity);

    /**
	 * Gets the depth.
	 *
	 * @return the depth
	 */
    public int getDepth();
}
