    private void jButtonNewActionPerformed(java.awt.event.ActionEvent evt) {
        mClassSetting++;
        mCardTemplateClassesData = new CardTemplateClassesData();
        clearAll();
        resetConfigPool(false, "");
        mClassSetting--;
    }

    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {
        readAllToCurrent();
        mCardTemplateClassesDataPool.put(mCardTemplateClassesData);
        mCardTemplateClassesDataPool.save();
        mClassSetting++;
        String klasse = jTextFieldKlasse.getText();
        resetConfigPool(true, klasse);
        jComboBoxName.setSelectedItem(mCardTemplateClassesData.mName);
        mClassSetting--;
    }

    private void jButtonSaveAsNewActionPerformed(java.awt.event.ActionEvent evt) {
        mCardTemplateClassesData = new CardTemplateClassesData();
        readAllToCurrent();
        mCardTemplateClassesDataPool.putAsNew(mCardTemplateClassesData);
        mCardTemplateClassesDataPool.save();
        mClassSetting++;
        String klasse = jTextFieldKlasse.getText();
        resetConfigPool(true, klasse);
        jComboBoxName.setSelectedItem(mCardTemplateClassesData.mName);
        mClassSetting--;
    }

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {
        readAllToCurrent();
        mCardTemplateClassesDataPool.remove(mCardTemplateClassesData);
        mCardTemplateClassesDataPool.save();
        mClassSetting++;
        String klasse = jTextFieldKlasse.getText();
        resetConfigPool(true, klasse);
        if (jComboBoxName.getSelectedIndex() == -1) {
            clearAll();
        }
        String key = jComboBoxName.getSelectedItem().toString();
        mCardTemplateClassesData = mCardTemplateClassesDataPool.get(key);
        setAllFromCurrent();
        mClassSetting--;
    }

    private void jComboBoxKlasseActionPerformed(java.awt.event.ActionEvent evt) {
        if (mClassSetting > 0) return;
        mClassSetting++;
        ;
        String selected = jComboBoxKlasse.getSelectedItem().toString();
        clearAll();
        resetConfigPool(true, selected);
        jTextFieldKlasse.setText(jComboBoxKlasse.getSelectedItem().toString());
        String key = jComboBoxName.getSelectedItem().toString();
        mCardTemplateClassesData = mCardTemplateClassesDataPool.get(key);
        setAllFromCurrent();
        mClassSetting--;
    }

    private void jComboBoxNameActionPerformed(java.awt.event.ActionEvent evt) {
        if (mClassSetting > 0) return;
        String key = jComboBoxName.getSelectedItem().toString();
        mCardTemplateClassesData = mCardTemplateClassesDataPool.get(key);
        setAllFromCurrent();
    }

