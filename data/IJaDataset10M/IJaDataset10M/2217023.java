    private void resetConfigPool(boolean select, String klasseToSet) {
        mClassSetting++;
        Collection<String> collectionKlasse = mCardTemplateClassesDataPool.getKlassenHashMap().values();
        Iterator<String> iterKlasse = collectionKlasse.iterator();
        int i = 0;
        String klasse = "";
        jComboBoxKlasse.removeAllItems();
        while (iterKlasse.hasNext()) {
            String item = iterKlasse.next();
            jComboBoxKlasse.addItem(item);
            if (select) {
                if (klasseToSet.length() == 0) {
                    if (i == 0) {
                        jComboBoxKlasse.setSelectedIndex(i);
                        jTextFieldKlasse.setText(item);
                        klasse = item;
                    }
                } else {
                    if (klasseToSet.equalsIgnoreCase(item)) {
                        jComboBoxKlasse.setSelectedIndex(i);
                        jTextFieldKlasse.setText(item);
                        klasse = item;
                    }
                }
            }
            i++;
        }
        if ((select) && (klasse.length() == 0)) {
            if (jComboBoxKlasse.getItemCount() > 0) {
                jComboBoxKlasse.setSelectedIndex(0);
                jTextFieldKlasse.setText(jComboBoxKlasse.getSelectedItem().toString());
                klasse = jComboBoxKlasse.getSelectedItem().toString();
            }
        }
        if (!select) jComboBoxKlasse.setSelectedIndex(-1);
        Collection<CardTemplateClassesData> colC = mCardTemplateClassesDataPool.getMapForKlasse(klasse).values();
        Iterator<CardTemplateClassesData> iterC = colC.iterator();
        jComboBoxName.removeAllItems();
        i = 0;
        while (iterC.hasNext()) {
            CardTemplateClassesData item = iterC.next();
            jComboBoxName.addItem(item.mName);
            if ((i == 0) && (select)) {
                jComboBoxName.setSelectedIndex(0);
                mCardTemplateClassesData = mCardTemplateClassesDataPool.get(item.mName);
                setAllFromCurrent();
            }
            i++;
        }
        if (!select) jComboBoxName.setSelectedIndex(-1);
        mClassSetting--;
    }

    private void clearAll() {
        mClassSetting++;
        mCardTemplateClassesData = new CardTemplateClassesData();
        setAllFromCurrent();
        mClassSetting--;
    }

    private void setAllFromCurrent() {
        mClassSetting++;
        jComboBoxKlasse.setSelectedItem(mCardTemplateClassesData.mClass);
        jTextFieldKlasse.setText(mCardTemplateClassesData.mClass);
        jComboBoxName.setSelectedItem(mCardTemplateClassesData.mName);
        jTextFieldName.setText(mCardTemplateClassesData.mName);
        mClassSetting--;
    }

    private void readAllToCurrent() {
        mCardTemplateClassesData.mClass = jTextFieldKlasse.getText();
        mCardTemplateClassesData.mName = jTextFieldName.getText();
    }

