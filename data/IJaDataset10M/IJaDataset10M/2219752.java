package org.proteored.miapeapi.xml.ge;

import org.proteored.miapeapi.interfaces.Equipment;
import org.proteored.miapeapi.xml.ge.autogenerated.MIAPEGEEquipmentType;
import org.proteored.miapeapi.xml.ge.util.GEControlVocabularyXmlFactory;
import org.proteored.miapeapi.xml.util.MiapeXmlUtil;

public class EquipmentImpl implements Equipment {

    private final MIAPEGEEquipmentType equipmentXML;

    public EquipmentImpl(MIAPEGEEquipmentType equipmentItem) {
        this.equipmentXML = equipmentItem;
    }

    @Override
    public String getCatalogNumber() {
        return equipmentXML.getCatalogNumber();
    }

    @Override
    public String getComments() {
        return equipmentXML.getComments();
    }

    @Override
    public String getDescription() {
        return equipmentXML.getDescription();
    }

    @Override
    public String getManufacturer() {
        return GEControlVocabularyXmlFactory.getName(equipmentXML.getManufacturer());
    }

    @Override
    public String getName() {
        return GEControlVocabularyXmlFactory.getName(equipmentXML.getName());
    }

    @Override
    public String getModel() {
        return GEControlVocabularyXmlFactory.getName(equipmentXML.getModel());
    }

    @Override
    public String getParameters() {
        return equipmentXML.getParameters();
    }

    @Override
    public String getUri() {
        return equipmentXML.getURI();
    }

    @Override
    public String getVersion() {
        return equipmentXML.getVersion();
    }

    @Override
    public int getId() {
        return MiapeXmlUtil.getIdFromXMLId(equipmentXML.getId());
    }
}
