    public ConnectorView(Connector pConnector) {
        initComponents();
        setFrameIcon(new util.Imagem().getFrameIcon());
        setTitle("Propriedades do Conector");
        setFramesConfiguration(Manager.getFramesConfiguration(getTitle()));
        connector = pConnector;
        setId(connector.getId());
        jtfID.setText(connector.getId());
        jtfNome.setText(connector.getName());
        jcoRelation.setSelectedItem(connector.getRelation());
        jtfOutGoing.setText(connector.getOutGoing());
        jtfTerminal.setText(connector.getTerminal());
    }

    /**
     * 
     * @return
     */
    @Override
    public boolean salvar() {
        connector.setName(jtfNome.getText());
        connector.setRelation(jcoRelation.getSelectedItem().toString());
        boolean updated = Manager.updateElement(connector);
        return updated;
    }

    /**
     * 
     */
    @Override
    public boolean fechar() {
        boolean fechar = false;
        getFramesConfiguration().setTitle(getTitle());
        getFramesConfiguration().setBounds(getBounds());
        getFramesConfiguration().setMaximum(isMaximum());
        Manager.updateFramesConfiguration(getFramesConfiguration());
        fechar = Manager.removeFrame(getId());
        if (fechar) {
            dispose();
        }
        return fechar;
    }

