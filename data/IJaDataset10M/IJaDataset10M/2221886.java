package server;

/**
 * 
 * The SSuprise contains the Data of an Suprise Object
 * 
 * @author ingo Breuer
 */
public class SSurprise {

    /**
     * X-Koor.
     */
    public int posx;

    /**
     * Y-Koor.
     */
    public int posy;

    /**
     * True if the Suprise is still active
     */
    public boolean active;

    /**
     * The Suprise Name
     */
    public String tag;

    /**
     * The Suprise Value ( not necessary)
     */
    public int value;

    /**
     * Teleporter
     */
    public int nposx;

    /**
     * Teleporter
     */
    public int nposy;
}
