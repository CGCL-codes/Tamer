package com.ecomponentes.formularios.lingua.to;

public class LinguaTO {

    public String idLingua;

    public String nomeLingua;

    public LinguaTO() {
        this.idLingua = "";
        this.nomeLingua = "";
    }

    public String getIdLingua() {
        return idLingua;
    }

    public void setIdLingua(String idLingua) {
        this.idLingua = idLingua;
    }

    public String getNomeLingua() {
        return nomeLingua;
    }

    public void setNomeLingua(String nomeLingua) {
        this.nomeLingua = nomeLingua;
    }
}
