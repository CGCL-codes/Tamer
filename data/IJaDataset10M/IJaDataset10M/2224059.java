package javango.http;

public class Http500 extends HttpException {

    public Http500() {
        super();
    }

    public Http500(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public Http500(String arg0) {
        super(arg0);
    }

    public Http500(Throwable arg0) {
        super(arg0);
    }
}
