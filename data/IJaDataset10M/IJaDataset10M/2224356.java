package org.hardtokenmgmt.ws.objects;

import java.util.List;

/**
 * Response object returned by a SelfServiceRequest WebService call.
 * 
 * Contains TokenCertificateResponseWS and HardTokenDataWS structures.
 * 
 * @author Philip Vendil Dec 12, 2011
 *
 * @version $Id$
 */
public class SelfServiceRequestResponse {

    private List<TokenCertificateResponseWS> tokenCertificateResponses;

    private HardTokenDataWS hardTokenDataWS;

    /**
	 * Empty constructor.
	 */
    public SelfServiceRequestResponse() {
    }

    /**
	 * Default constructor.
	 * 
	 * @param tokenCertificateResponses generated token responses
	 * @param hardTokenDataWS the hard token data WS, could be null if not needed by the requester.
	 */
    public SelfServiceRequestResponse(List<TokenCertificateResponseWS> tokenCertificateResponses, HardTokenDataWS hardTokenDataWS) {
        this.tokenCertificateResponses = tokenCertificateResponses;
        this.hardTokenDataWS = hardTokenDataWS;
    }

    /**
	  * 
	  * @param tokenCertificateResponses generated by the service.
	  */
    public void setTokenCertificateResponses(List<TokenCertificateResponseWS> tokenCertificateResponses) {
        this.tokenCertificateResponses = tokenCertificateResponses;
    }

    /**
	 * 
	 * @return TokenCertificateResponses generated by the service.
	 */
    public List<TokenCertificateResponseWS> getTokenCertificateResponses() {
        return tokenCertificateResponses;
    }

    /**
	 * 
	 * @param hardTokenDataWS hard token data of the token processed.
	 */
    public void setHardTokenDataWS(HardTokenDataWS hardTokenDataWS) {
        this.hardTokenDataWS = hardTokenDataWS;
    }

    /**
	 * 
	 * @return hard token data of the token processed, might be null depending on
	 * implementation of self service request.
	 */
    public HardTokenDataWS getHardTokenDataWS() {
        return hardTokenDataWS;
    }
}
