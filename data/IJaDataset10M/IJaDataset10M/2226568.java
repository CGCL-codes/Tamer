    private void anmeldenMenuItemActionPerformed(java.awt.event.ActionEvent evt) {
        Logger.trace("LogIn Action");
        Login login = new Login(this.getFrame(), true);
        login.setVisible(true);
        if (BusinessLayer.isLoggedIn()) {
            this.jTabbedPane1.setVisible(true);
            this.abmeldenItemMenu.setEnabled(true);
            this.anmeldenMenuItem.setEnabled(false);
            this.registerMenuItem.setEnabled(false);
            this.jTabbedPane1.addTab("Inventar", inventar);
            Logger.trace("Inventar Tab added");
            this.jTabbedPane1.addTab("Auction", auction);
            Logger.trace("Auction Tab added");
            this.jTabbedPane1.addTab("Account", account);
            Logger.trace("Account Info Tab added");
            if (BusinessLayer.isAdmin(BusinessLayer.getUserId())) {
                this.jTabbedPane1.addTab("Admin", admin);
                Logger.trace("Admin Tab added");
            }
            Logger.user(" ");
            refreshAll();
        }
    }

    public static final void refreshAll() {
        Logger.trace("RefreshAll()");
        inventar.BindTo();
        auction.BindTo();
        account.BindTo();
        admin.BindTo();
    }

    private void abmeldenItemMenuActionPerformed(java.awt.event.ActionEvent evt) {
        Logger.trace("Log off Action");
        business.abmelden();
        this.jTabbedPane1.setVisible(false);
        this.jTabbedPane1.removeAll();
        Logger.trace("Remove all Tabs");
        this.abmeldenItemMenu.setEnabled(false);
        this.anmeldenMenuItem.setEnabled(true);
        this.registerMenuItem.setEnabled(true);
    }

