package west.model.mmc;

/**
 *
 * @author armnant
 */
public class MmcFactoryException extends Exception {

    /**
     * Creates a new instance of <code>MmcFactoryException</code> without detail message.
     */
    public MmcFactoryException() {
    }

    /**
     * Constructs an instance of <code>MmcFactoryException</code> with the specified detail message.
     * @param msg the detail message.
     */
    public MmcFactoryException(String msg) {
        super(msg);
    }
}
