package com.haliyoo.adhs.exception;

public class DaoLevelException extends Exception {

    private static final long serialVersionUID = -1013594998926903687L;

    public DaoLevelException() {
        super();
    }

    public DaoLevelException(String arg0) {
        super(arg0);
    }

    public DaoLevelException(Throwable arg1) {
        super(arg1);
    }

    public DaoLevelException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }
}
