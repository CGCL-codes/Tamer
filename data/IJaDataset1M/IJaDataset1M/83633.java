package org.sadhar.sipp.perekamananggarankegiatanunit;

/**
 *
 * @author hendro
 */
public class Status {

    private String tahun;

    private String isRevisi;

    private String isDiajukan;

    public Status() {
    }

    /**
     * @return the tahun
     */
    public String getTahun() {
        return tahun;
    }

    /**
     * @param tahun the tahun to set
     */
    public void setTahun(String tahun) {
        this.tahun = tahun;
    }

    /**
     * @return the isRevisi
     */
    public String getIsRevisi() {
        return isRevisi;
    }

    /**
     * @param isRevisi the isRevisi to set
     */
    public void setIsRevisi(String isRevisi) {
        this.isRevisi = isRevisi;
    }

    /**
     * @return the isDiajukan
     */
    public String getIsDiajukan() {
        return isDiajukan;
    }

    /**
     * @param isDiajukan the isDiajukan to set
     */
    public void setIsDiajukan(String isDiajukan) {
        this.isDiajukan = isDiajukan;
    }
}
