package AlphaMiner;

/**
 *
 * @author XY
 */
public class DoubleRelation {

    private String start;

    private String end;

    public DoubleRelation(String start, String end) {
        this.start = start;
        this.end = end;
    }

    public String getEnd() {
        return end;
    }

    public void setEnd(String end) {
        this.end = end;
    }

    public String getStart() {
        return start;
    }

    public void setStart(String start) {
        this.start = start;
    }
}
