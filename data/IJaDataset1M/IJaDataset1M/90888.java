    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {
        if (evt.getValueIsAdjusting() == false) {
            if (jList1.getSelectedIndex() == -1) {
                jTable1.setModel(null);
            } else {
                String tableName = (String) jList1.getSelectedValue();
                getData(tableName);
            }
        }
    }

    private void getTables() {
        for (String table : DataBase.getTablesNames()) listModel.addElement(table);
    }

    private void getData(String tableName) {
        core.DataViewObj dataObj = DataBase.getTableData(tableName);
        int rows = dataObj.data.size();
        int cols = 0;
        if (rows > 0) cols = dataObj.data.get(0).size();
        Object data[][] = new Object[rows][cols];
        String title[] = new String[dataObj.title.size()];
        for (int i = 0; i < dataObj.title.size(); i++) title[i] = dataObj.title.get(i);
        for (int i = 0; i < dataObj.data.size(); i++) for (int j = 0; j < dataObj.data.get(i).size(); j++) data[i][j] = dataObj.data.get(i).get(j);
        jTable1.setModel(new javax.swing.table.DefaultTableModel(data, title));
    }

